<script>
    document.addEventListener('DOMContentLoaded', function() {
        'use strict';

        const filterChips = document.querySelectorAll('.filter-chip');
        const galleryCards = document.querySelectorAll('.gallery-card');
        const counts = {
            froid: 0,
            electricite: 0,
            'genie-civil': 0,
            construction: 0
        };

        // Compter les catégories
        galleryCards.forEach(card => {
            const categories = card.getAttribute('data-category').split(' ');
            categories.forEach(cat => {
                if (counts[cat] !== undefined) counts[cat]++;
            });
        });

        // Animer les compteurs
        Object.keys(counts).forEach(key => {
            const countEl = document.querySelector(`[data-count="${key}"]`);
            if (countEl) {
                let current = 0;
                const target = counts[key];
                const duration = 800;
                const increment = target / (duration / 16);

                const animate = () => {
                    current += increment;
                    if (current >= target) {
                        countEl.textContent = target;
                    } else {
                        countEl.textContent = Math.floor(current);
                        requestAnimationFrame(animate);
                    }
                };

                setTimeout(() => animate(), 400);
            }
        });

        // Filtrage
        filterChips.forEach(chip => {
            chip.addEventListener('click', function() {
                const filter = this.getAttribute('data-filter');

                filterChips.forEach(c => c.classList.remove('active'));
                this.classList.add('active');

                let visibleIndex = 0;

                galleryCards.forEach((card, idx) => {
                    const categories = card.getAttribute('data-category').split(' ');
                    const shouldShow = filter === '*' || categories.includes(filter);

                    setTimeout(() => {
                        if (shouldShow) {
                            card.classList.remove('hidden');
                            card.classList.add('visible');
                            card.style.setProperty('--index', visibleIndex++);
                        } else {
                            card.classList.add('hidden');
                            card.classList.remove('visible');
                        }
                    }, idx * 15);
                });
            });
        });

        // Intersection Observer
        if ('IntersectionObserver' in window) {
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('visible');
                        observer.unobserve(entry.target);
                    }
                });
            }, {
                threshold: 0.1,
                rootMargin: '50px'
            });

            galleryCards.forEach(card => observer.observe(card));
        } else {
            galleryCards.forEach(card => card.classList.add('visible'));
        }
    });
</script>
